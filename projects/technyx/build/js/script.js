const langBtn = document.getElementById("langBtn");
const langMenu = document.getElementById("langMenu");
const langLabel = document.getElementById("langLabel");

document.addEventListener("DOMContentLoaded", () => {
  const langMenuMobile = document.getElementById("langMenu");
  const langMenuDesktop = document.getElementById("langMenuDesktop");

  const langBtnMobile = document.getElementById("langBtn");
  const langBtnDesktop = document.getElementById("langBtnDesktop");

  const langLabelMobile = document.getElementById("langLabel");
  const langLabelDesktop = document.getElementById("langLabelDesktop");

  const menus = [langMenuMobile, langMenuDesktop];
  const buttons = [langBtnMobile, langBtnDesktop];

  // Toggle menu on click
  buttons.forEach((btn, index) => {
    if (!btn) return;
    btn.addEventListener("click", (e) => {
      e.stopPropagation();
      menus[index].classList.toggle("hidden");
    });
  });

  // Prevent closing menu when clicking inside
  menus.forEach((menu) => {
    if (!menu) return;
    menu.addEventListener("click", (e) => e.stopPropagation());
  });

  // Close dropdown when clicking outside
  document.addEventListener("click", () => {
    menus.forEach((menu) => menu?.classList.add("hidden"));
  });

  // Handle language selection
  const allLangOptions = document.querySelectorAll("[data-lang]");
  allLangOptions.forEach((option) => {
    option.addEventListener("click", () => {
      const lang = option.getAttribute("data-lang");

      if (langLabelMobile) langLabelMobile.textContent = lang.toUpperCase();
      if (langLabelDesktop) langLabelDesktop.textContent = lang.toUpperCase();

      changeLanguage(lang);

      menus.forEach((menu) => menu?.classList.add("hidden"));
    });
  });

  function changeLanguage(lang) {
    const elements = document.querySelectorAll("[data-i18n]");
    elements.forEach((el) => {
      const key = el.getAttribute("data-i18n");
      if (translations[lang]?.[key]) {
        el.textContent = translations[lang][key];
      }
    });
  }
});


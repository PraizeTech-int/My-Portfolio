// FAQ See More / See Less
const toggleBtn = document.getElementById("faq-toggle-btn");
const faqContainer = document.getElementById("faq-container");
const faqItems = faqContainer.querySelectorAll(".faq-item");
let expanded = false;

// Function to determine visible items
function updateFAQDisplay() {
  const screenWidth = window.innerWidth;
  const visibleCount = screenWidth <= 768 ? 5 : 8; // small vs large screens

  faqItems.forEach((item, index) => {
    if (!expanded && index >= visibleCount) {
      item.style.display = "none";
    } else {
      item.style.display = "block";
    }
  });

  // Let container height adjust automatically when expanded
  faqContainer.style.maxHeight = expanded ? "none" : "auto";

  // Show toggle button only if there are more items than visibleCount
  toggleBtn.style.display =
    faqItems.length > visibleCount ? "inline-block" : "none";
  toggleBtn.textContent = expanded ? "See Less" : "See More";
}

// Initial setup
updateFAQDisplay();

// Toggle button click
toggleBtn.addEventListener("click", () => {
  expanded = !expanded;
  updateFAQDisplay();
});

// Update on window resize
window.addEventListener("resize", () => {
  if (!expanded) updateFAQDisplay();
});
